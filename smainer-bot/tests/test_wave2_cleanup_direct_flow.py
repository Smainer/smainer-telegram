class TestWave2LegacyRollbackPreserved:
    """Toggling wallet_flow_direct=False restores legacy WebApp flow."""

    @pytest.mark.asyncio
    @patch("src.handlers.settings")
    async def test_legacy_flow_uses_webapp_button(self, mock_settings, deps):
        """When wallet_flow_direct=False, bot uses WebApp button."""
        mock_settings.wallet_flow_direct = False
        mock_settings.default_model = "llama3.1:8b"
        mock_settings.prompt_cost_strk = 100_000_000_000_000_000
        mock_settings.miniapp_url = "https://smainer-miniapp.vercel.app"
        mock_settings.get_miniapp_pay_url.return_value = (
            "https://smainer-miniapp.vercel.app/?action=pay&prompt=hi"
        )
        mock_settings.telemetry_sensitive_fields = False

        update = _make_update("Legacy test prompt")
        await handle_inference(
            update,
            deps["bot"],
            deps["wallet_mgr"],
            deps["payment_mgr"],
            deps["relayer"],
        )

        button = deps["bot"].edit_message_reply_markup.call_args[1]["reply_markup"].inline_keyboard[0][0]
        assert button.web_app is not None, "Legacy flow must use WebApp button"
        assert button.url is None, "Legacy flow must NOT use URL button"

    @pytest.mark.asyncio
    @patch("src.handlers.settings")
    async def test_feature_flag_toggle_direct_then_legacy(self, mock_settings, deps):
        """Toggle from direct to legacy restores WebApp button — no residual."""
        # Direct flow first
        mock_settings.wallet_flow_direct = True
        mock_settings.default_model = "llama3.1:8b"
        mock_settings.prompt_cost_strk = 100_000_000_000_000_000
        mock_settings.miniapp_url = "https://smainer-miniapp.vercel.app"
        mock_settings.get_direct_pay_url.return_value = "https://smainer-miniapp.vercel.app/?flow=direct"
        mock_settings.get_miniapp_pay_url.return_value = "https://smainer-miniapp.vercel.app/?action=pay"
        mock_settings.telemetry_sensitive_fields = False

        update = _make_update("Toggle test")
        await handle_inference(
            update,
            deps["bot"],
            deps["wallet_mgr"],
            deps["payment_mgr"],
            deps["relayer"],
        )

        button1 = deps["bot"].edit_message_reply_markup.call_args[1]["reply_markup"].inline_keyboard[0][0]
        assert button1.url is not None, "First call should be URL (direct)"

        # Reset mock and switch to legacy
        deps["bot"].reset_mock()
        msg = MagicMock()
        msg.message_id = 1001
        deps["bot"].send_message.return_value = msg
        mock_settings.wallet_flow_direct = False

        await handle_inference(
            update,
            deps["bot"],
            deps["wallet_mgr"],
            deps["payment_mgr"],
            deps["relayer"],
        )

        button2 = deps["bot"].edit_message_reply_markup.call_args[1]["reply_markup"].inline_keyboard[0][0]
        assert button2.web_app is not None, "Second call should be WebApp (legacy rollback)"
        assert button2.url is None


