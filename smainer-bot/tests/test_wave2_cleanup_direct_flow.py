class TestWave2DirectFlowNoApproveUI:
    """User never sees MiniApp approve/open-Braavos UI in the direct flow."""

    @pytest.mark.asyncio
    @patch("src.handlers.settings")
    async def test_direct_flow_url_contains_flow_direct(self, mock_settings, deps):
        """Direct flow pay URL must contain flow=direct to trigger auto-redirect."""
        mock_settings.wallet_flow_direct = True
        mock_settings.default_model = "llama3.1:8b"
        mock_settings.prompt_cost_strk = 100_000_000_000_000_000
        mock_settings.miniapp_url = "https://smainer-miniapp.vercel.app"
        mock_settings.get_direct_pay_url.return_value = (
            "https://smainer-miniapp.vercel.app/?action=pay&flow=direct"
            "&prompt=test&tier=small&chat_id=67890&message_id=999&nonce=test-nonce-w2"
        )
        mock_settings.telemetry_sensitive_fields = False

        update = _make_update("Test compute task")
        await handle_inference(
            update,
            deps["bot"],
            deps["wallet_mgr"],
            deps["payment_mgr"],
            deps["relayer"],
        )

        deps["bot"].edit_message_reply_markup.assert_called_once()
        call_kwargs = deps["bot"].edit_message_reply_markup.call_args[1]
        keyboard = call_kwargs["reply_markup"]
        button = keyboard.inline_keyboard[0][0]

        # Must be a URL button, not WebApp
        assert button.url is not None, "Direct flow must use URL button"
        assert button.web_app is None, "Direct flow must NOT use WebApp button"
        assert "flow=direct" in button.url

